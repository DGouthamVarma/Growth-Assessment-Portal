package Bean;

public class CenturyFluenciesBean {
	private int otherPerson;
	private int grammar;
	private int conversation;
	private int writtenInfo;
	private int questioning;
	private int emailWriting;
	public int getOtherPerson() {
		return otherPerson;
	}
	public void setOtherPerson(int otherPerson) {
		this.otherPerson = otherPerson;
	}
	public int getGrammar() {
		return grammar;
	}
	public void setGrammar(int grammar) {
		this.grammar = grammar;
	}
	public int getConversation() {
		return conversation;
	}
	public void setConversation(int conversation) {
		this.conversation = conversation;
	}
	public int getWrittenInfo() {
		return writtenInfo;
	}
	public void setWrittenInfo(int writtenInfo) {
		this.writtenInfo = writtenInfo;
	}
	public int getQuestioning() {
		return questioning;
	}
	public void setQuestioning(int questioning) {
		this.questioning = questioning;
	}
	public int getEmailWriting() {
		return emailWriting;
	}
	public void setEmailWriting(int emailWriting) {
		this.emailWriting = emailWriting;
	}
}
